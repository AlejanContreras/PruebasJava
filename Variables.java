public class Variables {
    public static void main(String[] args) {
        //Como crear una variable
        int numero = 5;

        //mostar numero en el terminal
        System.out.println(numero);
        System.out.println();
        //TIPOS DE DATOS
        // numericos
        byte bite = 127;
        System.out.println(bite);
        System.out.println();
        
        short chort = 32767;
        System.out.println(chort);
        System.out.println();
        
        int entero = -8765321;
        System.out.println(entero);
        System.out.println();
        
        long largo = 987654321;
        System.out.println(largo);
        System.out.println();
        
        float decimalF = 3.141592F;
        System.out.println(decimalF);
        System.out.println();
        
        double decimalD = 2.71828;
        System.out.println(decimalD);
        System.out.println();


        //booleanos
        boolean verdadero = true;
        System.out.println(verdadero);
        System.out.println();

        boolean falso = false;
        System.out.println(falso);
        System.out.println();

        //char o caracter
        char caracter = 'A';
        System.out.println(caracter);
        System.out.println();

        // Strigns o texto
        String texto = "Arroz Con Pollo";
        System.out.println(texto);
        System.out.println();

        //TIPOS DE REFERENCIA
        // da la cantidad de caracteres
        System.out.println(texto.length());
        // todo el texto en mayuscula
        System.out.println(texto.toUpperCase());
        // todo el texto en minuscula
        System.out.println(texto.toLowerCase());
        // 
        System.out.println();
        //
        System.out.println();
        //
        System.out.println();

    }
}
